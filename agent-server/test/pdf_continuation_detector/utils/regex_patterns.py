"""
国家标准/技术规范常见结构的正则表达式模式

提供用于识别国家标准、技术规范等文档中常见结构的正则表达式。
"""

import re
from typing import Pattern, Optional


# ============================================================================
# 章节标题模式
# ============================================================================

# 章节标题正则表达式
# 用途：匹配国家标准/技术规范中的章节标题格式
# 匹配示例：
#   - "5" (单级编号)
#   - "5.2" (二级编号)
#   - "5.2.3" (三级编号)
#   - "A.1" (附录编号)
#   - "A.2.1" (附录多级编号)
#   - "第五章" (中文章节)
#   - "第5章" (中文章节带数字)
# 说明：支持数字编号、字母编号（附录）、中文章节等多种格式
SECTION_TITLE_PATTERN: Pattern = re.compile(
    r"^"
    r"(?:"
    r"  第[一二三四五六七八九十\d百千万]+[章节部分条]|"  # 第五章、第5章、第一百条
    r"  \d+(?:\.\d+){0,2}|"                              # 5、5.2、5.2.3
    r"  [A-Z](?:\.\d+){1,2}|"                            # A.1、A.2.1
    r"  [一二三四五六七八九十]+[、．.]"                      # 一、二、三、
    r")"
    r"(?:\s|$)",  # 后面跟空格或行尾
    re.VERBOSE
)


# ============================================================================
# 列表项模式
# ============================================================================

# 列表项正则表达式
# 用途：匹配列表项的开头格式，用于识别列表的延续
# 匹配示例：
#   - "a)" (小写字母+右括号)
#   - "b)" (小写字母+右括号)
#   - "c)" (小写字母+右括号)
#   - "A)" (大写字母+右括号)
#   - "a." (字母+点号)
# 说明：主要用于识别有序列表的延续，判断列表是否跨页
LIST_ITEM_PATTERN: Pattern = re.compile(
    r"^"
    r"\s*"  # 允许前导空格
    r"[a-zA-Z]"  # 单个字母（大小写均可）
    r"[\)\.]"  # 右括号或点号
    r"\s+"  # 后面必须有空格
)


# ============================================================================
# 表格延续模式
# ============================================================================

# 表格延续标记正则表达式
# 用途：匹配表格跨页时的延续标记，用于识别表格的跨页延续
# 匹配示例：
#   - "续表" (中文续表标记)
#   - "表 X（续）" (表编号+续标记)
#   - "表1（续）" (表编号+续标记)
#   - "(续)" (单独的续标记)
#   - "续表 X" (续表+编号)
# 说明：识别表格跨页时的各种标记格式
CONTINUED_TABLE_PATTERN: Pattern = re.compile(
    r"(?:"
    r"  续表\s*(?:[A-Z]\.?\d*|\d+)?|"      # 续表、续表1、续表A、续表A.1
    r"  表\s*[A-Z]?\.?\d+\s*[（(]续[）)]|"  # 表1（续）、表A(续)、表A.1（续）
    r"  [（(]续[）)]"                      # (续)、（续）
    r")",
    re.VERBOSE
)


# ============================================================================
# 未完成句子模式
# ============================================================================

# 未完成句子结尾正则表达式
# 用途：匹配未完成句子的结尾标记，用于识别句子跨页延续
# 匹配示例：
#   - "：" (冒号，常用于引出下文)
#   - "，" (逗号，句子中间停顿)
#   - "、" (顿号，列举项分隔)
#   - "以及" (连接词，表示还有内容)
#   - "如下" (引出下文)
#   - "包括" (引出列举)
#   - "见表" (引出表格引用)
# 说明：识别句子未完成的常见结尾模式，这些模式通常表示句子会在下一页继续
UNFINISHED_SENTENCE_PATTERN: Pattern = re.compile(
    r"(?:"
    r"  [：，、]|"           # 冒号、逗号、顿号
    r"  以及|"               # 连接词
    r"  如下|"               # 引出下文
    r"  包括|"               # 引出列举
    r"  见表|"               # 表格引用
    r"  见[图表]|"          # 图表引用
    r"  等[，。]?|"          # 等（列举未完成）
    r"  及[，。]?|"          # 及（列举未完成）
    r"  和[，。]?|"          # 和（列举未完成）
    r"  或[，。]?|"          # 或（列举未完成）
    r"  其中|"               # 引出详细说明
    r"  具体|"               # 引出具体内容
    r"  如下[所]?示|"        # 如下所示
    r"  如[下上]|"           # 如下、如上
    r"  详见|"               # 详见
    r"  参见"                # 参见
    r")"
    r"\s*$",  # 行尾，可能有空格
    re.VERBOSE
)


# ============================================================================
# 辅助函数
# ============================================================================

def is_section_title(text: str) -> bool:
    """
    判断文本是否为章节标题
    
    Args:
        text: 要检查的文本
        
    Returns:
        是否为章节标题
    """
    if not text:
        return False
    
    text = text.strip()
    if not text or len(text) > 100:  # 标题通常不会太长
        return False
    
    return bool(SECTION_TITLE_PATTERN.match(text))


def is_list_item(text: str) -> bool:
    """
    判断文本是否为列表项
    
    Args:
        text: 要检查的文本
        
    Returns:
        是否为列表项
    """
    if not text:
        return False
    
    text = text.strip()
    return bool(LIST_ITEM_PATTERN.match(text))


def is_continued_table(text: str) -> bool:
    """
    判断文本是否包含表格延续标记
    
    Args:
        text: 要检查的文本
        
    Returns:
        是否包含表格延续标记
    """
    if not text:
        return False
    
    return bool(CONTINUED_TABLE_PATTERN.search(text))


def is_unfinished_sentence(text: str) -> bool:
    """
    判断文本是否以未完成句子的结尾结尾
    
    Args:
        text: 要检查的文本
        
    Returns:
        是否以未完成句子的结尾结尾
    """
    if not text:
        return False
    
    # 检查最后一行
    lines = [line.strip() for line in text.split('\n') if line.strip()]
    if not lines:
        return False
    
    last_line = lines[-1]
    return bool(UNFINISHED_SENTENCE_PATTERN.search(last_line))


def extract_section_title(text: str) -> Optional[str]:
    """
    从文本中提取章节标题
    
    Args:
        text: 文本内容
        
    Returns:
        章节标题，如果没有则返回 None
    """
    if not text:
        return None
    
    lines = text.split('\n')
    for line in lines:
        line = line.strip()
        if is_section_title(line):
            return line
    
    return None
